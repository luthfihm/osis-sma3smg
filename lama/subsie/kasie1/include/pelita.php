Pelita adalah salah satu subsie di SMA Negeri 3 Semarang yang merupakan wadah dari seluruh siswa beragama Katolik. Subsie ini diketuai oleh  Robertus Irwan kelas XI IA 5. Subsie yang berjumlah anggota 29 anak ini berkumpul setiap hari Jumat dan menyelenggarakan bakti sosial.<br /><br />
Subsie ini mempunyai beberapa kegiatan pokok yaitu Retreat, Natal, dan Ziarah. Retreat dilaksanakan pada bulan Agustus, Natal pada bulan Desember, dan Ziarah pada bulan Oktober. Tujuan utama dari subsie ini sendiri adalah untuk mempererat persaudaraan antar siswa dan siswi beragama Katolik di SMA Negeri 3 Semarang dan untuk memperkuat iman di jiwa siswa siswi Katolik.



