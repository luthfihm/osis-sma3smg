2. Mengikuti lomba setingkat kota, provinsi, nasional, dll. <br />
3. Pengarsipan hasil karya <br />
4. Mengerjakan desain sesuai permintaan (pamflet, poster,     kalender, spanduk, co-card, dll.) <br /><br />
