             Tennis meja atau yang biasa disebut ping pong ini, terlihat mudah dan santai, tetapi banyak orang yang pasti berkembang dengan baik dan mengikuti beberapa event-event.
Event-event besar yang ada tiap tahunnya yaitu Porsimaptar, OOSN, dan Pemda. Untuk waktu dan tempatnya, Porsimaptar diadakan di masing-masing kota, karena kita di Semarang, jadi diadakannya di Semarang.<br /><br />
OOSN tempatnya tiap tahun berubah, biasanya diselenggarakan di Kota Jakarta.
Pemda diadakan di Semarang, tetapi untuk waktunya tidak tentu.<br /><br />
Untuk pesertanya, Kak Teguh bilang diajukan 3 orang dari tim tennis meja, 1 tim SINGLE dan 1 tim GANDA.
Tim tennis meja smaga, dalam rangka akan mengikuti event-event tersebut, telah mempersiapkan tim nya agar menjadi lebih mantab, yaitu dengan cara latihan sore, 3-4 kali seminggu.<br /><br />
Untuk latian rutinnya, dilaksanakan di Aula SMA 3, tiap sore setiap hari Senin, Kamis, dan Sabtu. Untuk kegiatan lainnya selain kegiatan rutin tidak ada, tetapi pasti tiap anggotanya sering berkumpul untuk mempererat kebersamaan dan kekompakkan.<br /><br />


