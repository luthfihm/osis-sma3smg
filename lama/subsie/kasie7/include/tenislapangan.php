Subsie Tennis Lapangan adalah salah satu ekstrakurikuler di SMA 3 tercinta ini. Subsie ini sudah tentu melatih anak - anak SMA 3 yang berminat, bahkan berbakat dalam tennis lapangan. Dengan bantuan Pak Eko Djamsari sebagai pelatih, diharapkan peserta ekstrakurikuler ini dapat mahir dalam olahraga tennis, khususnya tennis lapangan<BR /><br />

Mayoritas peserta adalah anak - anak kelas X, sebagian diantaranya ikut karena ingin meneruskan bermain tennis, dan ada juga yang bahkan  belum mengerti tennis sama sekali. Subsie ini mengadakan latihan setiap hari Sabtu, pada jam 7 pagi. Ini karena untuk mencegah hari panas, yang dapat berdampak pada kesehatan para pesertanya. Kegiatan subsie ini selain latihan rutin, adalah menonton pertandingan - pertandingan tennis kelas dunia. Program ini adalah program terobosan subsie tennis pada tahun ini. Selain sebagai suatu sarana refreshing,  diharapkan para peserta dapat mempelajari permainan petenis kelas dunia macam Roger Federer, Rafael Nadal, Andy Roddick, dll. <br /><br />

Oya, mereka juga tidak hanya berlatih. Jika ada perlombaan tennis antar sekolah, subsie ini biasanya mengirimkan delegasinya untuk berkompetisi dalam turnamen tersebut, terutama PORSIMAPTAR. Jadi, para peserta subsie ini juga dilatih mentalnya dalam mengikuti turnamen - turnamen tersebut. <br />
