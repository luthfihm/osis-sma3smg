mso-no-proof:yes'>Apa sih J-Soul Matsuri 4 itu?<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-US
style='mso-no-proof:yes'>J-Soul Matsuri 4 adalah salah satu acara dalam
rangkaian acara Pekan raya Smaga (PRS), dimana acaranya sendiri yaitu sebuah
acara festival kebudayaan yang diadakan oleh anak-anak MOCHI, yang mana
acaranya sendiri memadukan antara budaya lokal Indonesia, khususnya Jawa,
dengan budaya Jepang. Tahun ini sendiri adalah kali keempat MOCHI mengadakan
acara J-Soul Matsuri. Pada tahun ini, tema yang di usung adalah PANIC IN SOUL,
yang merupakan singkatan dari Japan Futuristic Indonesia Soul. J-Soul Matsuri 4
diadakan pada hari Minggu tanggal 9 Oktober 2011 yang lalu.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify'><i style='mso-bidi-font-style:
normal'><span lang=EN-US style='font-size:12.0pt;line-height:115%;font-family:
"Brush Script MT";mso-no-proof:yes'>Ada apa aja sih di J-Soul Matsuri 4?<o:p></o:p></span></i></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-US
style='mso-no-proof:yes'>Banyak banget dong! Mulai dari lomba-lomba, hiburan
dari MOCHI, penampilan dari para Guest Star yang oke punya, serta beraneka
jenis stand-stand yang ada, mulai dari stand makanan, minuman, permainan,
bahkan kerajinan tangan juga ada. Selain stand, pada tahun ini, MOCHI
bermotivasi untuk membuat sebuah acara baru, dan acara ini yaitu adanya Obake,
yang artinya Rumah Hantu, dan alhamdulillah responnya disambut dengan cukup
antusias.<o:p></o:p></span></p>

<p class=MsoNormal style='text-align:justify'><i style='mso-bidi-font-style:
normal'><span lang=EN-US style='font-size:12.0pt;line-height:115%;font-family:
"Brush Script MT";mso-no-proof:yes'>Bagaimana urutan acaranya?<o:p></o:p></span></i></p>

