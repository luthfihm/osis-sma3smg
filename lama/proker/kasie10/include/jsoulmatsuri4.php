<p class=MsoNormal style='text-align:justify'><span lang=EN-US
style='mso-no-proof:yes'>Setelah itu, pada pukul 10, lomba band diadakan.
Setelah 2 band telah tampil, lalu diselingi oleh lomba dance dan suguhan
para-para yang diadakan oleh anggota MOCHI. Setelah itu, saat jarum jam
menunjuk angka 11, parade cosplay pun dimulai. Setelah itu disusul oleh lomba
manga, otaku, yugi-oh, para-para, modern dance, dan sebagian band yang belum
tampil. Dan jam tiga sore, Guest Star tahun ini pun muncul dan semakin
memeriahkan suasana. Setelah itu, pengumuman pemenang pun di bacakan. Para
peserta yang menang pun boleh berbangga hati. Setelah riuh acara berlangsung, seperti
halnya sebuah pesta, acara pun ditutup, saat jam menunjuk waktu setengah lima
sore.<o:p></o:p></span></p>

