Sabtu-Minggu tanggal 15-16 Mei lalu, OSIS SMA Negeri 3 Semarang telah mengadakan Latihan Kepemimpinan Siswa 2 OSIS (LKS 2 OSIS) yang merupakan program kerja terkait dengan regenerasi pengurus OSIS. Kegiatan ini merupakan lanjutan dari LKS 1 OSIS dan diikuti oleh peserta yang telah lolos seleksi awal.<br /><br />
<img src="http://farm5.static.flickr.com/4136/4804420748_88f8ca9b16.jpg" class="img-subsie" />
Kegiatan diawali dengan apel, dibuka oleh Bapak Drs. Hari Waluyo, Kepala Sekolah SMA N 3 Semarang selaku Pembina apel. Pada acara selanjutnya, peserta LKS 2 OSIS diandaikan sebagai pengurus OSIS, dimana mereka dituntut untuk mempersiapkan sebuah kegiatan Program Kerja OSIS, dimulai dari pembuatan proposal dan mempresentasikan kegiatan mereka pada pihak yang terkait dengan kegiatan tersebut, seperti sekolah dan sponsor.<br /><br />
Sebagai seorang calon-calon pengurus OSIS SMA Negeri 3 Semarang selanjutnya, para peserta diharapkan dapat mengerti seluk beluk OSIS SMA Negeri 3 Semarang melalui acara selanjutnya, yaitu presentasi pengurus OSIS. Pemahaman ini diperdalam lagi dalam acara sharing bersama alumni OSIS SMA Negeri 3 Semarang.<br /><br />
Selanjutnya, para peserta mengikuti Workshop, yang dibawakan oleh alumni OSIS SMA Negeri 3 Semarang yaitu Rizki Wahyudi (Ketua OSIS periode 2007/2008) dan Raras (Ketua Seksi Berbangsa dan Bernegara OSIS periode 2007/2008).<br /><br />
<img src="http://farm5.static.flickr.com/4082/4803790219_d0ed0bfe2b.jpg" class="img-subsie" />
Keesokan harinya, peserta LKS mengikuti games outbond yang dipandu oleh pengurus OSIS. Dalam games itu mereka dilatih untuk saling berkoordinasi dan bekerjasama dalam mencapai satu tujuan. Di permainan itu bisa tampak bagaimana karakter seseorang itu sesungguhnya. Dan diharapkan dari games tersebut para peserta bisa mengambil maknanya untuk diterapkan dalam kepengurusan OSIS selanjutnya.<br /><br />
Setelah acara games tersebut, disambung dengan acara pentas seni yang ditampilkan oleh seluruh peserta LKS 2 OSIS. Kemudian apel penutupan. Dan acara terakhir adalah pengumuman, dimana akhirnya terpilihlah 18 besar Calon Mitratama OSIS SMA Negeri 3 Semarang. Dan 18 besar yang telah terpilih adalah:<br />
