		  <b>I.	VISI</b><br />
Menjadi SSC yang beribawa, bertanggung jawab, tegas, dan berjiwa sosial baik dalam kehidupan formal maupun informal. Serta menjaga silaturahmi anggota antar angkatan.<br /><br />

<b>II.	MISI</b><br />
1.	Mengadakan pertemuan rutin.<br />
2.	Mengadakan makrab dengan alumni<br />
3.	Menjaga almamater SMA Negeri 3 Semarang.<br />
4.	Berdisiplin dan berbudi pekerti luhur.<br />
5.	Tetap menjaga kebersamaan dan kekeluargaan antar anggota<br /><br />

<b>III.	TUGAS</b><br />
Menjaga keamanan dan kenyamanan di lingkungan SMA Negeri 3 Semarang, khususnya pada setiap event yang diadakan oleh OSIS dan pihak sekolah baik event tahunan, maupun insidental seperti GPLB, PAB, PENSAGA, dan lain-lain.<br /><br />

<b>IV.	PROFIL SSC</b><br />
School Security Club (SSC) merupakan salah satu subseksi non-ekstra di SMA Negeri 3 Semarang di bawah naungan Organisasi Siswa Intra Sekolah (OSIS). SSC terdiri dari sekelompok siswa yang bertanggung jawab atas keamanan di lingkungan SMA Negeri 3 Semarang, khususnya pada setiap event yang diadakan oleh OSIS dan pihak sekolah baik event tahunan, maupun insidental seperti GPLB, PAB, PENSAGA, dan lain-lain. Keberadaan SSC dalam setiap event sangatlah penting karena tanpa keberadaan SSC, maka koordinasi keamanan tidak akan berjalan dengan baik . <br /><br />
<img src="http://farm5.static.flickr.com/4080/4803995830_96cb129e78.jpg" class="img-subsie"/><br /><br />

<b>V.	SEJARAH SSC</b><br />
SSC berdiri pada tahun 2000. Berisikan sekumpulan siswa SMA 3 yang peduli pada keamanan sekolah. Semula organisasi ini belum resmi diakui sebagai salah satu subseksi di SMA Negeri 3 Semarang. Dan diresmikan pada tahun 2009 dengan pembina Bapak Djumasri. 


