	Subsie rohani Buddha adalah perkumpulan umat Buddha yang ada di SMA Negeri 3 Semarang. Rohani Buddha ini khusus ditujukan buat siswa-siswi SMA Negeri 3 Semarang yang beragama Buddha. Karena jumlah siswa yang beragama Buddha di sekolah tak banyak, maka subsie ini bergabung dengan perkumpulan remaja Buddha di Semarang yang bernama Muda Mudi Remaja Buddish Semarang. <br /><br />
	Bersama rekan-rekan umat buddha dari berbagai sekolah di Semarang dan dibimbing oleh Ibu Sri Kuntini, Rohani Buddha mendapat pelajaran keagamaan yang pastinya bisa menambah keimanan dan wawasan tentang agama Buddha. Selain itu, pada hari besar Waisak diadakan doa bersama di Candi Borobudur dan pada hari-hari besar agama Buddha lainnya diadakan kunjungan ke vihara- vihara yang berada di luar kota Semarang. Selain bisa nambah pengalaman, juga bisa mempererat persaudaraan antar umat Buddha di kota Semarang dan Jawa Tengah.

