KPS KAPA merupakan kepanjangan dari Kelompok Pemuda Sebaya Kesatuan Pelajar Anti Narkoba. Subsie KPS KAPA merupakan subsie yang baru dibentuk tahun ini atas rekomendasi Dinas Pemuda dan Olahraga Provinsi Jawa Tengah. 
Status KPS KAPA adalah unit kegiatan siswa dan bagian dari OSIS, bersifat non partisipan (tidak ada hubungan dengan organisasi politik manapun).<br /><br />

Latar Belakang
•	Penyalahgunaan narkotika, psikotropika dan zat adiktif berbahaya dewasa ini telah menjadi fenomena yang sangat berbahaya dan mengancam kelangsungan hidup serta masa depan seseorang bahkan masa depan bangsa.<br />
•	Penyalahguna berasal dari berbagai umur, lapisan sosial ekonomi, dan pendidikan.<br /><br />

Latar Belakang<br />
•	Jumlah penyalahguna paling besar adalah kalangan generasi muda yang berusia antara 10-25 tahun.<br />
•	Dengan demikian KPS KAPA bisa diharapkan mampu menumbuhkan rasa bangga dan kepercayaan diri para siswa khususnya di kalangan pengurus dan anggota. <br /><br />

Tujuan Pembentukan KPS KAPA <br />
•	Sosialisasi bahaya narkoba dan penyimpangan perilaku remaja di sekolah. <br />
•	Mengurangi pengaruh dan gerak peredaran gelap narkoba di sekolah. <br />
•	Memberikan kesadaran siswa terhadap semua pengaruh dan akibat narkoba. <br />
•	Memberikan kegiatan alternatif dan positif kepada siswa.<br /><br />

Tugas dan Fungsi KPS KAPA<br />
•	Meningkatkan pengetahuan dan kesadaran siswa terhadap bahaya narkoba dan penyimpangan perilaku remaja.<br />
•	Mendorong siswa untuk berani menolak setiap ajakan dan paksaan untuk mencoba serta terlibat dalam penyalahgunaan narkoba.<br />
•	Menginformasikan pada pihak sekolah apabila ada siswa yang dicurigai melakukan perilaku penyimpangan remaja.<br /><br />

Bentuk Kegiatan KPS KAPA<br />
•	Pengenalan, pemahaman, dan sosialisasi tentang narkoba di sekolah.<br />
•	Penyebaran informasi dan pengertian tentang bahaya narkoba di sekolah.<br />
•	Pemantauan terhadap kelompok negatif di sekolah dan tempat berkumpulnya.<br />
•	Pendidikan dan keterampilan.<br />



