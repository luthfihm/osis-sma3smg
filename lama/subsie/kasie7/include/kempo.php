Kempo, adalah salah satu dari beberapa subsie beladiri yang ada di SMA Negeri 3 Semarang. Seni beladiri yang satu ini bisa dibilang adalah induk dari semua seni beladiri yang ada. Kempo diciptakan oleh seorang guru besar dari India, yang sedang berkelana. Diperjalanan beliau banyak sekali melihat permasalahan manusia, maka beliau menciptakan suatu gerakan gerakan untuk manusia membela diri.<br /><br />
	Subsie Kempo, sudah berdiri sangat lama di SMA Negeri 3 Semarang. Maka sudah cukup banyak prestasi yang dicetak dari subsie ini. Diantaranya ialah, juara II emwhu tingkat kota, juara III randori, dan masih banyak lagi yang lainnya.<br /><br />
	Sebagai Induk dari semua seni beladiri, Kempo memiliki ciri yang unik dibanding beladiri yang lainnya. Meninju, menendang, membanting, semua ada dalam beladiri yang satu ini.


