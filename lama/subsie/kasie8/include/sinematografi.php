Sinematografi adalah subsie yang mempelajari tentang pembuatan dari sebuah film. Subsie ini didirikan di SMA Negeri 3 Semarang pada tahun 2000 oleh bapak Sholeh Amin. Namun baru baru diresmikan pada tanggal 15 Agustus 2002. Pembina subsie Sinematografi saat ini adalah bapak Edi Susanto.<br /><br />
	Sejak Sinematografi berdiri hingga sekarang, sudah banyak prestasi yang diraih oleh subsie ini. Subsie ini pernah berpartisipasi dalam ajang perfilman yang bergengsi, yaitu FFI pada tahun 2004. Selain itu subsie ini juga pernah menjadi peringkat 7 dalam event FFPJ (Festifal Film Pelajar Jateng).<br /><br />
	Sinematografi mempunyai sebuah kegiatan yaitu movie camp. Movie camp adalah semacam acara kemah, yang di acara tersebut pesertanya diajari teknik - teknik dalam proses pembuatan sebuah film. Banyak hal yang bisa kita dapat dalam acara movie camp ini.

    


