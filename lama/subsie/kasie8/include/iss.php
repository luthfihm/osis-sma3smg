Ikatan Suporter Smaga atau yang sering disebut ISS merupakan ajang berkumpulnya anak-anak yang selalu semangat dan ceria, dalam menyemangati subsie-subsie olah raga yang ada di smaga ketika bertanding.<br /><br />
 ISS selalu andil dalam event-event yang diadakan subsie-subsie olah raga, seperti basket, sepak bola, voli dll. Ketika akan diadakan sebuah event baisanya mereka berkumpul untuk membahas event, membuat yel-yel, latihan bersama dll.<br /><br />
 Biasanya waktu dan tempatnya tak tentu. ISS yang anggota tetapnya juga SSC ini bermanfaat sekali lho, khususnya untuk subsie-subsie olah raga, karena dapat meningkatkan semangat mereka ketika bertanding. So menjadi anggota ISS mempunyai banyak jaringan pertemanan yaitu dengan subsie-subsie olah raga, SSC, dan juga antar supporter sekolah lain. Subsie yang punya kendala emosi ini selalu meningkatkan kepartisipasian mereka dalam setiap pertandingan untuk menjaga keeksisannya. <br /><br />
Harapan untuk kedepannya adalah ISS lebih ditunjang, sukses, semakin kompak, ramai dan seru. Tentunya tak ada ISS di pertandingan tak ramai dan seru.


