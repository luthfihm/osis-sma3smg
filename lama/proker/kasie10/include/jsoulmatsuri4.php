<p class=MsoNormal style='text-align:justify'><span lang=EN-US
style='mso-no-proof:yes'>Acara ini dimulai pukul delapan pagi, yang diawali
dengan doa dan sambutan-sambutan dari Ketua MOCHI, Ketua Pelaksana, Pembina
MOCHI, dan dari Waka. Setelah itu, para peserta bisa daftar ulang lalu
berkeliling stand sambil menunggu giliran lomba. Omikoshi pun diarak keliling
lapangan depan sekolah oleh para anggota MOCHI. Omikoshi, perlambangan
festival. Semoga acara ini berjalan sukses.<o:p></o:p></span></p>

