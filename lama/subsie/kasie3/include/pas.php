<b>SEJARAH</b><br  />
	 Subsie Perwakilan Ambalan Soeringgit sebenarnya merupakan satuan tugas dari Dewan Kerja Ambalan Soeringgit (KS 149) yang mengurus bidan kepramukaan. Karena kedudukan OSIS dan Ambalan Soeringgit sejajar, diperlukan suatu satuan tugas yang bertugas sebagai wakil Ambalan Soeringgit. Maka dibentuklah Perwakilan Ambalan Soeringgit (PAS) yang diketuai oleh Wakil Pradhana putra Ambalan Soeringgit selaku Kasubsie PAS. Hal ini disahkan dan disepakati bersama dalam Surat Keputusan Bersama (SKB) OSIS-Ambalan Soeringgit.<br  /><br  />
<b>PROFIL SUBSIE PAS</b><br  />
	Subsie PAS berada di bawah naungan OSIS dan berkoordinasi dengan Sie 3. Pengurus OSIS. Tugas-tugas yang diamanatkan yaitu sebagai penghubung antara OSIS dan Ambalan Soeringgit, meningkatkan dan menjalin hubungan baik antara kedua belah pihak, serta menyepakati dan mengadakan kerjasama OSIS-Ambalan Soeringgit yang saling menguntungkan.


