Perisai Diri Cabang Semarang yang mana unit SMA 3 Semarang termasuk di dalamnya dimulai dengan Angkatan Pelopor yang terdiri dari 10 orang. Mereka merupakan adik dan teman dekat dari Guanawan Parikesit, pendiri Perisai Diri Cabang Semarang. Tempat latihan mereka di halaman rumah di Jalan Gisiksari I/23 Semarang.<br /><br />
Untuk prestasi yang sudah dicapai Subsie ini, adalah kejuaraan tingkat Propinsi yang telah diraih tahun lalu. Namun saat ini, Kak Gifari sedang mempersiapkan guna kejuaraan selanjutnya. Kali ini masih tingkat Kota. Tim Perisai Diri akan mengirimkan kontingennya dari anggota kelas XI dan kelas X. Untuk usaha penyeleksiannya, adalah dengan mengukur berat badan. Karena lomba ini dikategorikan menurut berat badan. Dan biasanya untuk siswa kelas X, mereka akan mengikuti lomba dalam bidang seni gerakan. Semoga bisa meraih prestasi yang gemilang ke depannya, Amin.

