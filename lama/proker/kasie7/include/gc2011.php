Acara puncak Ganesha Cup tersendiri pada Hari Minggu, 23 Oktober 2011 yang berisikan pada lomba SMP yaitu final Futsal, Bulutangkis, dan Bola Voly. Dan pada lomba SMA terdapat lomba Solo Vocal. Lomba Poster SMP dan SMA pun baru dimulai pada hari itu juga.</br></br>
Ganesha Cup merupakan Program Terobosan dari Kasie 7 periode 2010/2011.</br>



