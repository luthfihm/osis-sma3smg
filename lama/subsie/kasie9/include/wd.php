Webdesign secara resmi terbentuk tahun 2009. Sebelumnya webdesign merupakan suatu kesatuan dengan IOT, yang mana terbagi menjadi IOT - pascal dan IOT - webdesign.<br /><br />
Materi utama yang dipelajari dalam ekstrakurikuler ini adalah mengenai pembuatan web page. Pada masa kini, internet bukanlah suatu hal yang asing bagi seluruh generasi. Dan salah satu unsur penting dalam penggunaan internet adalah webpage. Siswa yang mengikuti ekstrakurikuler ini akan mempelajari mulai dari dasar-dasar html dengan Notepad, Dreamweaver, hingga tingkatan yang lebih sulit seperti penggunaan database, Java, MySQL, dan lain-lain.<br /><br />
Pembuatan web page membutuhkan kreatifitas untuk dapat membuat web yang enak dilihat. Selain itu, Webdesign juga telah menjadi salah satu bahan yang akan dipelajari dalam mata pelajaran TIK pada kelas 3 semester 1. Jadi, siswa sudah sekaligus disiapkan dalam menghadapi pelajaran itu.


