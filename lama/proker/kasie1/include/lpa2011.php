		Apa itu LPA ? Bagaimana LPA itu?</br></br>
		
	LPA merupakan kepanjangan dari Lomba Pendidikan Agama. Lomba ini merupakan lomba agama pertama di Indonesia dan diperuntukan bagi siswa-siswi SMP se-Kota Semarang yang beragama Islam, Kristen dan Katholik un tuk mewujudkann rasa persaudaraan dan kesatuan sebagai wujud pengamalan Pancasila ke-3.</br></br>
