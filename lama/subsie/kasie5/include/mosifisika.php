SMAN 3 Semarang merupakan salah satu sekolah terfavorit di kota Semarang. SMAN3  Semarang dikenal punya reputasi yang baik di bidang pendidikan akademik, maupun non akademik. Banyak alumni dari SMAN 3 Semarang yang sudah mengecap manis hasil pendidikan yamg telah mereka jalani, baik dalam karir atau kesempatan mendapatan pendidikan lebih tinggi di Universitas terkemuka.<br /><br />
 	Untuk memperoleh hasil terbaik tentu sarana untuk membangkitkan dan mengembangkan potensi-potensi para siswa smaga juga diperlukan. Terbentuklah MOSI (Menuju Olimpiade Sains) Fisika sebagai wadah para siswa pecinta Fisika SMAN 3 berkumpul dan belajar bersama, mengasah ilmu, dan berkompetisi di bidangnya.  <br /><br />
	Anggota MOSI Fisika adalah semua siswa smaga yang mencintai pelajaran Fisika dan ingin mempelajari fisika secara lebih dalam, untuk mengerti konsep dan menantang diri sendiri untuk menyelesaikan soal fisika Olimpiade yang penuh trik dan menggelitik nalar. Selain itu, para anggota juga diberi kesempatan mengikuti kompetisi fisika antar SMA, sehingga mereka bisa membuktikan dan 'melepas' kemampuan yang mereka latih di SMA 3.<br /><br />
	Beberapa anggota sudah memberikan prestasi dan turut mengharumkan nama SMAN 3 Semarang dan banyak anggota ynag merasa lebih mengerti Fisika dan mendapatkan nilai lebih baik di pelajaran sekolah.



