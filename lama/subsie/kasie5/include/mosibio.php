MOSI BIOLOGI adalah salah satu subseksi yang bertujuan untuk menghimpun siswa-siswa yang berminat dalam biologi yang kemudian akan diikutkan lomba-lomba yang bertajuk biologi mewakili sekolah.<br /><br />

Sebenarnya terdapat juga subseksi KIR Biologi namun karena pembekalan materi serta praktek yang saling berkaitan, mulai tahun lalu, KIR dan MOSI Biologi dijadikan satu dalam MOSI BIOLOGI.<br /><br />

Subseksi ini telah mengukir berbagai prestasi diantaranya:<br />
•	Meraih medali emas OSN<br />
•	Meraih medalu perak OSN<br />
•	Meraih medali perunggu OSN<br />
•	Juara I dan II lomba mapel biologi tingkat kota semarang<br />
•	Semifinalis IBSC2009 UGM<br />
•	dll<br /><br />

