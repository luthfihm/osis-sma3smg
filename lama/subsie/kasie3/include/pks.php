PKS (Patroli Keamanan Sekolah) yang terbentuk 28 tahun yang lalu itu merupakan salah satu subsie tertua di SMA 3 Semarang. Bersemboyankan "aron simegah nan tapati di dada para satrya pinandhita dari bumi ganesha", PKS menjelma menjadi salah atu ekskul paling eksis di SMA 3 Semarang, bahkan menjadi salah satu PKS paling eksis di kota Semarang juga Jawa Tengah.<br  /><br  />

PKS sangat menjunjung tinggi jiwa ksatriya, loyalitas, dan kebersamaan. Dalam PKS kita tidak hanya diajarkan Pelajaran Baris Berbaris dan mengatur lalu lintas, tapi juga diajarkan caranya berorganisasi, berbicara didepan umum, bagaimana memecahkan masalah dalam waktu singkat, belajar mengenali teman.<br  /><br  />

PKS SMA 3 Semarang juga mempunyai banyak kegiatan. Sebagai salah satu subsie 4p, PKS mengadakan seleksi, mengikuti Demo Satu November saat HUT SMA 3 tercinta dan Pelantikan Anggota Bersama dengan OSIS, serta ada LDK (latihan dasar kepemimpinan) dan STJ (serah terima jabatan). Sedang kegiatan lain seperti, SIM kolektif dimana PKS beerjasama dengan Satlantas Semarang untuk membantu siswa siswi SMA 3 Semarang yang ingin mempunyai SIM, latihan gabungan dengan PKS SMA lain, dan lomba "gebyar ganesha" se-Jawa tengah yang akan dilaksanakan dalam waktu dekat. PKS juga mempunyai program rutin yaitu latihan rutin setiap hari selasa dan sabtu, patroli berkeliling SMA 3 setiap upacara, dan tugas jalan di gerbang depan dan belakang SMA 3 untuk mengatur lalulintas.<br  /><br  />

So, mari kita dukung terus budaya berorganisasi dan tertib lalulintas di SMA 3 Semarang lewat PKS.


