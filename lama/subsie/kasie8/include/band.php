Subsie band merupakan sebuah subsie kesenian yang cukup banyak diminati oleh para siswa dan siswi SMA Negeri 3 Semarang. Didalam subsie ini terdapat berbagai macam band – band dengan berbagai macam aliran musik seperti pop, jazz, rock, serta musik – musik pop – rock bernuansa Jepang. Subsie band biasa mengadakan pertemuan pada pukul 08.00 sampai selesai, pada hari Sabtu. Perkumpulan diadakan diluar sekolah karena sekolah belum terdapat studio musik serta perlengkapan band yang belum memadahi. Mereka berkumpul di studio – studio musik di daerah Sompok atau menyewa studio di tempat lain. Band juga turut menyumbangkan berbagai prestasi untuk SMA Negeri 3 Semarang kita tercinta ini. Beberapa prestasinya yaitu Juara II festival band dan the best keyboard se- Jateng 2005, Juara II festival band tingkat kota tahun 2005, serta Juara favorit dan best guitar pada tahun 2006.<br /><br />
 	Event rutin yang diadakan oleh subsie band yaitu lomba band pada saat classmeeting. Lomba tersebut bertujuan untuk menyalurkan bakat siswa dan siswi SMA Negeri 3 di bidang musik pastinya. Dalam lomba ini untuk memenangkannya diperlukan beberapa kriteria seperti aransemen dan keselarasan nada.<br /><br />
	Setiap tahunnya, band juga memiliki andil dalam pagelaran besar yang diadakan SMA Negeri 3 yaitu Pensaga.



