Ganesha Softball and Baseball Club (GASOCA) sudah berlangsung selama 7 tahun mulai tahun 2004. Sekarang GASOCA dilatih oleh Bu Endang SMA 3, namun terkadang juga mendapatkan pelatihan dari COMOS (tim softball dan baseball Semarang) juga dari alumni. Selama ini GASOCA telah meraih berbagai prestasi, diantaranya juara 2 softball putri tingkat SMA se-Semarang (2009), juara harapan 1 baseball putra tingkat SMA se-Semarang (2010), juara 3 softball putra tingkat umum se-Jateng (2010), dll.<br /><br />
	Latihan rutin GASOCA diadakan tiap hari Jumat, Sabtu, dan Minggu, kalau Jumat latihan di AKPOL, Sabtu di sekolah, sedangkan Minggu untuk latihan tambahan. Selama ini subsi GASOCA juga telah menjalin hubungan dengan alumni terdahulu, jadi dalam 7 angkatan ini sudah saling kenal satu sama lain. GASOCA juga sering mengadakan sparring dengan alumni maupun sekolah lain di Semarang.
