	Beberapa lomba Ganesha Cup untuk SMP, yaitu :</br>
1. Futsal</br>
2. Bola Voly</br>
3. Tennis Meja</br>
4. Bulu Tangkis</br>
5. Poster</br></br>
Sedangkan lomba Ganesha Cup untuk SMA, yaitu :</br>
1. Poster</br>
2. Solo vocal</br></br>
Pelaksanaan Ganesha Cup itu sendiri dilaksanakan pada tanggal 20-23 Oktober 2011 lalu yang sebelumnya terdapat juga fase Play off yang dilaksanakan pada tanggal 19 Oktober. Cabang Play off sendiri merupakan hanya dari cabang lomba futsal.</br></br>
