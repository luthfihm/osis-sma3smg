        <div style="overflow:auto; height:500px; width: 505px"><p class="no_marg s1" style="width:475px">
		Ganesha Cup adalah serangkaian acara Pekan Raya SMA 3 yang berkonsepkan Olahraga dan Seni. Acara ini ditujukan untuk para Pelajar SMP dan SMA guna untuk mengembangkan potensi siswa-siswa serta meramaikan acara Pekan Raya SMA 3 itu sendiri.</br></br>
